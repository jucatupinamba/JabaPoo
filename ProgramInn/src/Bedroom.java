
public class Bedroom {

	private String name;
	private String email;


	public Bedroom(String name, String email) {
		this.name = name;
		this.email = email;
	}


	public Bedroom() {
	
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getEmail() {
		return email;
	}


	public void setEmail(String email) {
		this.email = email;
	}
}


	

